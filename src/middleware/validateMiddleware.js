const { validationResult } = require('express-validator');
const { registerSchema, loginSchema, createProductSchema, updateProductSchema, updateProfileSchema } = require('../utils/validationSchemas');

const validateRegister = [
  registerSchema,
  (req, res, next) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({ errors: errors.array() });
    }
    next();
  },
];

